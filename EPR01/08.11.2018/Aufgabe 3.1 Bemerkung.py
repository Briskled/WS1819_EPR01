n = 6

# Das hier dürft ihr NICHT tun!!!
while True:
     durchschnitt = (n+1)/2
     print(durchschnitt)
     break

# Warum klappt es trotzdem?
# Die kleine Gauss-Formel besagt, dass die Summe der ersten n natürlichen Zahlen
# berechnet werden können durch: n(n+1)/2
# Der Durchschnitt dessen wird berechnet, indem das Ergebnis durch n geteilt wird
# n(n+1)/2n = (n+1)/2
# In dieser Aufgabe müsst ihr aber wirklich alle Zahlen addieren!!
